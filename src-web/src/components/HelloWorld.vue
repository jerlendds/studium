        if (checkParent(target, navMenu)) {
            // click on the link
            if (navMenuDiv.classList.contains("hidden")) {
                navMenuDiv.classList.remove("hidden");
            } else {
                navMenuDiv.classList.add("hidden");
            }
        } else {
            // click both outside link and outside menu, hide menu
            navMenuDiv.classList.add("hidden");
        }
    }
}

function checkParent(t, elm) {
    while (t.parentNode) {
        if (t == elm) {
            return true;
        }
        t = t.parentNode;
    }
    return false;
}

export default {
    name: 'HelloWorld',

    data() {
        return {
            navMenuDiv: this.$refs.navContent,
            navMenu: this.$refs.navToggle
        }
    },

    mounted() {
        var scrollpos = window.scrollY;
        var header = document.getElementById("header");
        var navcontent = document.getElementById("nav-content");
        var navaction = document.getElementById("navAction");
        var toToggle = document.querySelectorAll(".toggleColour");

        document.addEventListener("scroll", function () {
            /*Apply classes for slide in bar*/
            scrollpos = window.scrollY;

            if (scrollpos > 10) {
                header.classList.add("bg-white-50");
                navaction.classList.remove("bg-white-50");
                navaction.classList.add("gradient");
                navaction.classList.remove("text-gray-800");
                navaction.classList.add("text-lightgray-50");
                //Use to switch toggleColour colours
                for (var idx = 0; idx < toToggle.length; idx++) {
                    toToggle[idx].classList.add("text-gray-800");
                    toToggle[idx].classList.remove("text-lightgray-50");
                }
                header.classList.add("shadow");
                navcontent.classList.remove("bg-white-200");
                navcontent.classList.add("bg-white-50");
            } else {
                header.classList.remove("bg-white-50");
                navaction.classList.remove("gradient");
                navaction.classList.add("bg-white-50");
                navaction.classList.remove("text-lightgray-50");
                navaction.classList.add("text-gray-800");
                //Use to switch toggleColour colours
                for (var i = 0; i < toToggle.length; i++) {
                    toToggle[i].classList.add("text-lightgray-50");
                    toToggle[i].classList.remove("text-gray-800");
                }

                header.classList.remove("shadow");
                navcontent.classList.remove("bg-white-50");
                navcontent.classList.add("bg-transparent");
            }
        });

        document.onclick = self.check;
    },

    methods: {
        checkParent: function (t, elm) {
            while (t.parentNode) {
                if (t == elm) {
                    return true;
                }
                t = t.parentNode;
            }
            return false;
        },

        check: function (e) {
            var target = (e && e.target) || (event && event.srcElement);
            //Nav Menu
            if (!self.checkParent(target, self.navMenuDiv)) {
                // click NOT on the menu
                if (self.checkParent(target, self.navMenu)) {
                    // click on the link
                    if (self.navMenuDiv.classList.contains("hidden")) {
                        self.navMenuDiv.classList.remove("hidden");
                    } else {
                        self.navMenuDiv.classList.add("hidden");
                    }
                } else {
                    // click both outside link and outside menu, hide menu
                    self.navMenuDiv.classList.add("hidden");
                }
            }
        }
    }
}

